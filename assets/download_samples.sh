#!/bin/bash
# Download all 60 samples from GSE71378 (Snyder et al. 2016)
# Uses /opt/miniconda3/bin/fastq-dump with --split-3 to ensure balanced paired-end files

set -e
OUTDIR="$(dirname "$0")"
READS=1000000

ACCESSIONS=(
    SRR2129993 SRR2129994 SRR2129995 SRR2129996 SRR2129997
    SRR2129998 SRR2129999 SRR2130000 SRR2130001 SRR2130002
    SRR2130003 SRR2130004 SRR2130005 SRR2130006 SRR2130007
    SRR2130008 SRR2130009 SRR2130010 SRR2130011 SRR2130012
    SRR2130013 SRR2130014 SRR2130015 SRR2130016 SRR2130017
    SRR2130018 SRR2130019 SRR2130020 SRR2130021 SRR2130022
    SRR2130023 SRR2130024 SRR2130025 SRR2130026 SRR2130027
    SRR2130028 SRR2130029 SRR2130030 SRR2130031 SRR2130032
    SRR2130033 SRR2130034 SRR2130035 SRR2130036 SRR2130037
    SRR2130038 SRR2130039 SRR2130040 SRR2130041 SRR2130042
    SRR2130043 SRR2130044 SRR2130045 SRR2130046 SRR2130047
    SRR2130048 SRR2130049 SRR2130050 SRR2130051 SRR2130052
)

echo "Downloading ${#ACCESSIONS[@]} samples to $OUTDIR"

for SRR in "${ACCESSIONS[@]}"; do
    if [ -f "${OUTDIR}/${SRR}_1.fastq.gz" ]; then
        echo "Skipping $SRR — already downloaded"
        continue
    fi
    echo "Downloading $SRR..."
    /opt/miniconda3/bin/fastq-dump --split-3 --gzip -X $READS --outdir $OUTDIR $SRR
    echo "Done: $SRR"
done

echo "All downloads complete."